import FullScreenLayout from '@/components/layout/FullScreenLayout.vue'
import CommonGridShape from '@/components/common/CommonGridShape.vue'
import { ref } from 'vue'
import { useRouter } from 'vue-router'
import { useUserStore } from '@/store/userStore';

const firstName = ref('')
const lastName = ref('')
const username = ref('')
const email = ref('')
const password = ref('')
const showPassword = ref(false)
const agreeToTerms = ref(false)
const router = useRouter()
const userStore = useUserStore();

const togglePasswordVisibility = () => {
  showPassword.value = !showPassword.value
}

const handleSignup = async () => {
  const trimmedEmail = email.value.trim();

  if (!firstName.value || !lastName.value || !username.value || !trimmedEmail || !password.value) {
    alert('Mohon lengkapi semua bidang yang wajib diisi.');
    return;
  }
  if (!agreeToTerms.value) {
    alert('Anda harus menyetujui Syarat dan Ketentuan serta Kebijakan Privasi.');
    return;
  }

  console.log('Signup.vue: Calling userStore.register...');
  const success = await userStore.register(
    trimmedEmail,
    password.value,
    username.value,
    firstName.value,
    lastName.value
  );

  if (success) {
    if (userStore.error && userStore.error.includes('verifikasi')) {
       alert(userStore.error); // Menampilkan pesan verifikasi email jika ada
    } else {
       alert('Pendaftaran berhasil! Silakan login.'); // Pesan default sukses
    }
    router.push('/signin');
  } else {
    // userStore.error sudah diset di userStore jika ada kegagalan
    alert('Pendaftaran gagal: ' + (userStore.error || 'Terjadi kesalahan tidak dikenal.'));
  }
}
